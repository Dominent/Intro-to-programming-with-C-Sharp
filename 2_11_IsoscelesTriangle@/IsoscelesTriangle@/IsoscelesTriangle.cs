using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace IsoscelesTriangle
{
    class IsoscelesTriangle
    {
        static void Main(string[] args)
        {
            char c = '\u00A9';
            Console.WriteLine();
            Console.WriteLine("\t\t\t        " + c           );
            Console.WriteLine("\t\t\t       " + c + " "   + c);
            Console.WriteLine("\t\t\t      " + c + "\t  " + c);
            Console.WriteLine("\t\t\t     " + c + "\t   " + c);
            Console.WriteLine("\t\t\t    " + c + "\t    " + c);
            Console.WriteLine("\t\t\t   " + c + "\t     " + c);
            Console.WriteLine("\t\t\t  " + c + "\t      " + c);
            Console.WriteLine("\t\t\t " + c + "\t       " + c);
            Console.WriteLine("\t\t\t" + c + "\t\t" + c);
            Console.WriteLine("\t\t\t"+c+c+c+c+c+c+c+c+c+c+c+c+c+c+c+c+c);
            Console.WriteLine();
        }
    }
}
